    private void reset_pass_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reset_pass_btnActionPerformed
        String name = Name_TF.getText();
        String pass = password_field.getText();
        String UID = User_ID_TF.getText();
        String pass_confirm = Password_Field2.getText();
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/lab_management", "root", "root");
            Statement stmt = con.createStatement();
            if (pass.matches(pass_confirm) == false) {
                JOptionPane.showMessageDialog(null, "Password not matched");
            } else {
                String sw = JOptionPane.showInputDialog(null, "Enter Security Word:");
                String word = "select * from admin_id where Security_word = '"+sw+"' and UserID = '"+UID+"';";
                String qry = "update admin_id set Password = '" + pass + "' where UserID = '" + UID + "' and Security_word = '"+sw+"';";
                ResultSet rs = stmt.executeQuery(word);
                if (rs.next()) {
                    stmt.executeUpdate(qry);
                    JOptionPane.showMessageDialog(null, "Password Reset Successfull");
                    Login_page info = new Login_page();
                    info.setVisible(true);
                    this.dispose();
                } 
                else {
                    JOptionPane.showMessageDialog(null, "Invalid Security Word", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_reset_pass_btnActionPerformed

