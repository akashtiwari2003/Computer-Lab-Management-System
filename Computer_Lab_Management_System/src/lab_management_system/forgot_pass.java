    private void Check_details_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Check_details_btnActionPerformed
        String name = Name_TF.getText();
        String UID = User_ID_TF.getText();
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/lab_management", "root", "root");
            Statement stmt = con.createStatement();
            String qry = "select * from admin_id where Name = '" + name + "' and UserID = '" + UID + "';";
            if (Name_TF.getText().equals(null) || User_ID_TF.getText().equals(null)) {
                JOptionPane.showMessageDialog(null, "Details are not filled.", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                ResultSet rs = stmt.executeQuery(qry);
                if (rs.next()) {
                    password_field.setEnabled(true);
                    Password_Field2.setEnabled(true);
                    reset_pass_btn.setEnabled(true);
                    JOptionPane.showMessageDialog(null, "Enter your new password!");
                    Name_TF.setEnabled(false);
                    User_ID_TF.setEnabled(false);
                } else {
                    JOptionPane.showMessageDialog(null, "Name or UserID did not matched.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_Check_details_btnActionPerformed

